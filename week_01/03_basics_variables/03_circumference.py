'''
Write the necessary code that prints out the area and circumference
of a circle with a radius of 3.14

'''
import math
radius = 3.14
pi = math.pi
print(pi * 3.14 ** 2)
print("Is the area of a circle with the radius of 3.14")

print(2 * pi * radius)
print("Is the circumference of a circle with the radius of 3.14")
